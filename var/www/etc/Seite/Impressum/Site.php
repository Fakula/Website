<h2>Session-Login-Token</h2>
<p>Im Falle einer Anmeldung auf unserer Website wird ein technisch notwendiges Session-Login-Token verwendet, um die Authentifizierung des Nutzers sicherzustellen. Ohne dieses Token wäre eine Anmeldung nicht möglich. Das Token enthält Informationen, die zur Identifizierung des Nutzers notwendig sind und wird für die Dauer der Sitzung gespeichert. Nach dem Logout oder dem Ablauf der Sitzung wird das Token automatisch gelöscht.</p>
